function dodawanie(a, b) {
    return a + b;
}

function odejmowanie(a, b) {
    return a - b;
}

function mnozenie(a, b) {
    return a * b;
}

function dzielenie(a, b) {
    return a / b;
}
console.log(dodawanie(1, 2));
console.log(odejmowanie(1, 2));
console.log(mnozenie(1, 2));
console.log(dzielenie(1, 2));